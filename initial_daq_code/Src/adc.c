/**
  * @brief  set buffer pointers, main and secondary and configure DMA's to write ADC info there
  * @author SM
  * @param  None
  * @note   Not used
  * @retval None
  */

void ADC_configDMA(void)
{
  HAL_ADC_Start_DMA(&hadc, (uint32_t*) (adc_dmaBuffer), (uint32_t)sizeof(adc_dmaBuffer));
}

/**
  * @brief  set buffer pointers, main and secondary and configure DMA's to write ADC info there
  * @author SM
  * @param  None
  * @note   Not used
  * @retval None
  */

void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc)
{
	uint16_t i; 
	memcpy (&adc_workingBuffer, &adc_dmaBuffer, sizeof(adc_dmaBuffer));
	memset (&adc_avgBuffer, 0, sizeof(adc_avgBuffer));
	for (i = 0; i < ADC_RAW_BUFFER_LENGTH;i++)
	{
		adc_avgBuffer[i % ADC_MAX_CHANNELS] += adc_workingBuffer[i];// + (ADC_MAX_CHANNELS * j)];
	}
	for (i = 0; i < ADC_MAX_CHANNELS; i++)
	{
		adc_dataBuffer[i] = (uint16_t) adc_avgBuffer[i] / ADC_SAMPLES_PER_CHANNEL;	
	}
	
}
uint16_t adc_getBatVolts(uint8_t batteryNumber)
{
	return (adc_avgBuffer[batteryNumber]);
}
uint16_t adc_getThermistor(uint8_t thermistorNumber)
{
	return (adc_avgBuffer[thermistorNumber + 10]); //10 channel offset
}
uint16_t adc_getEmg(uint8_t emgNumber)
{
	return (adc_avgBuffer[emgNumber + 8]); //8 channel offset
}
uint16_t adc_getTouchSense (uint8_t tsNumber)
{
	return (adc_avgBuffer[tsNumber + 6]); //6 channel offset
}

