void TIM_initTimers(void)
{
	TIM1->CCR1 = 0;	//
	HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
	TIM1->CCR2 = 0;	// 
	HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_2);	
	TIM1->CCR3 = 0;	//
	HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_3);
	TIM1->CCR4 = 0;	// 
	HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_4);	
}
void TIM_setCh1PWM (uint16_t dutyCycle)
{
	TIM1->CCR1 = dutyCycle * 24;	//Auto Reload Register value divided by 100
}
void TIM_setCh2PWM (uint16_t dutyCycle)
{
	TIM1->CCR2 = dutyCycle * 24;	//Auto Reload Register value divided by 100
}
